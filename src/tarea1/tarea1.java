    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        //JOptionPane.showMessageDialog(rootPane, jTextField1.getText());
        double valor     = Double.parseDouble(jTextField1.getText());
        double resultado = 0;

        for(double i = 1; i <= valor; i++){
            resultado+= 1/i;
        }
        jTextField2.setText(String.format("%.5g%n",resultado));

        resultado = 0;
        for(double i = 1; i <= valor; i++){
            resultado+= i/Math.pow(2,i);
        }
        jTextField3.setText(String.format("%.5g%n",resultado));

    }//GEN-LAST:event_jButton2MouseClicked

